package people;

public class Person {
	//Attributes
	private String name;
	private int age;
	private String jobTitle;
	
	//Constructors
	public Person(String name, int age, String jobTitle) {
		this.name = name;
		this.age = age;
		this.jobTitle = jobTitle;		
	}
	
	//Methods

	public String getName() {
		return this.name;
	}

		
	
}
